class RunningAroundPark
{
public:
	int numberOfLap(int N, vector <int> d)
	{
        int laps = 1;
        for (int i = 0; i < d.size() - 1; i++) {
            if (d[i+1] <= d[i]) {
                laps++;
            }
        }
		return laps;
	}
};


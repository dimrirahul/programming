class ElectronicPetEasy
{
public:
	string isDifficult(int st1, int p1, int t1, int st2, int p2, int t2)
	{
        REP(i, t1) {
            REP (j, t2) {
                int x = st1 + p1 * i;
                int y = st2 + p2 * j;
                if ( x == y ) return string("Difficult");
                if (y > x) break;
            }
        }
		return string("Easy");
	}
};

